package bean;

public class NoticeVo {
	int mnSerial;
	String mnSubject;
	String mnContent;
	String mnDate;
	int mnView;
	
	
	public int getMnSerial() {
		return mnSerial;
	}
	public void setMnSerial(int mnSerial) {
		this.mnSerial = mnSerial;
	}
	public String getMnSubject() {
		return mnSubject;
	}
	public void setMnSubject(String mnSubject) {
		this.mnSubject = mnSubject;
	}
	public String getMnContent() {
		return mnContent;
	}
	public void setMnContent(String mnContent) {
		this.mnContent = mnContent;
	}
	public String getMnDate() {
		return mnDate;
	}
	public void setMnDate(String mnDate) {
		this.mnDate = mnDate;
	}
	public int getMnView() {
		return mnView;
	}
	public void setMnView(int mnView) {
		this.mnView = mnView;
	}
	
	
}
